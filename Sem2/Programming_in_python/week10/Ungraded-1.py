
class Calculator:

	@staticmethod
	def Sum(a, b):
		return a + b

	@staticmethod
	def Subtraction(a, b):
		return a - b

	@staticmethod
	def Multiply(a, b):
		return a * b

	@staticmethod
	def Division(a, b):
		return a / b

	@staticmethod
	def Power(a, b):
		return a ** b

	@staticmethod
	def Remainder(a, b):
		return a % b 

	@staticmethod
	def Quotient(a, b):
		return a // b

